// Derives small, name/description-only "search index" files from the full preset/tutorial
// data (which also carries the heavy node graph / step data). Pages that only need to
// list or search by name/description — globalSearch.js, the troubleshoot cross-links, the
// encyclopedia's "used in tutorial" links — import these instead of the full 62+85-file
// barrels, so they don't pull down graph/step data they never touch.
//
// Also derives nodeTypeUsage.js: a { typeId: presetId[] } reverse index so the encyclopedia's
// "used in these presets" section doesn't need every preset's full graph just to find which
// ones contain a given node type.
//
// Generated output is committed (this is a static site with no build step), so this script
// doubles as a drift check: `node scripts/generate-search-index.mjs --check` regenerates in
// memory and fails if it doesn't match what's committed — run in CI so a new preset/tutorial
// added without re-running this script fails the build instead of silently going stale.
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import presets from "../data/presets/index.js";
import tutorials from "../data/tutorials/index.js";

const repoRoot = path.resolve(fileURLToPath(import.meta.url), "../..");
const checkOnly = process.argv.includes("--check");

// preset id -> source filename (e.g. "molten_gold" -> "molten_gold.js"), so pages that only
// need a handful of specific presets (the landing page's hero/gallery) can dynamically
// import() just those files by id instead of pulling in the full 62-file barrel. Not simply
// `${id}.js` — a few files don't match their preset's id (metal.js exports id "brushed_metal",
// etc.) — so this is derived from data/presets/index.js's actual import/export statements
// rather than assumed by convention.
async function derivePresetFileMap() {
  const src = await readFile(path.join(repoRoot, "data/presets/index.js"), "utf8");
  const varToFile = new Map();
  for (const m of src.matchAll(/^import (\w+) from "\.\/([\w.-]+\.js)";$/gm)) {
    varToFile.set(m[1], m[2]);
  }
  const exportMatch = src.match(/export default \[([\s\S]*?)\];/);
  if (!exportMatch) throw new Error("couldn't find `export default [...]` in data/presets/index.js");
  const orderedVars = exportMatch[1].split(",").map((s) => s.trim()).filter(Boolean);
  if (orderedVars.length !== presets.length) {
    throw new Error(`data/presets/index.js export list has ${orderedVars.length} entries but presets array has ${presets.length} — parsing assumption broke`);
  }
  const idToFile = {};
  orderedVars.forEach((varName, i) => {
    const file = varToFile.get(varName);
    if (!file) throw new Error(`no import found for "${varName}" (used in export default list)`);
    idToFile[presets[i].id] = file;
  });
  return idToFile;
}
const presetFileMap = await derivePresetFileMap();

const HEADER = "// AUTO-GENERATED by scripts/generate-search-index.mjs — do not hand-edit.\n// Run `node scripts/generate-search-index.mjs` after adding/renaming/removing a preset or tutorial.\n";

function jsLiteral(value, indent = 0) {
  if (value === undefined) return "undefined";
  const pad = "  ".repeat(indent);
  const pad1 = "  ".repeat(indent + 1);
  if (Array.isArray(value)) {
    if (value.length === 0) return "[]";
    return `[\n${value.map((v) => `${pad1}${jsLiteral(v, indent + 1)},`).join("\n")}\n${pad}]`;
  }
  if (value && typeof value === "object") {
    const keys = Object.keys(value);
    return `{ ${keys.map((k) => `${k}: ${jsLiteral(value[k], indent)}`).join(", ")} }`;
  }
  return JSON.stringify(value);
}

const presetSearchIndex = presets.map((p) => ({ id: p.id, name: p.name, description: p.description, file: presetFileMap[p.id] }));
const tutorialSearchIndex = tutorials.map((t) => ({ id: t.id, name: t.name, description: t.description, level: t.level }));

const nodeTypeUsage = {};
for (const preset of presets) {
  for (const node of preset.graph.nodes) {
    if (!nodeTypeUsage[node.typeId]) nodeTypeUsage[node.typeId] = [];
    if (!nodeTypeUsage[node.typeId].includes(preset.id)) nodeTypeUsage[node.typeId].push(preset.id);
  }
}

const outputs = [
  {
    file: "data/presets/searchIndex.js",
    content: `${HEADER}export default ${jsLiteral(presetSearchIndex)};\n`,
  },
  {
    file: "data/tutorials/searchIndex.js",
    content: `${HEADER}export default ${jsLiteral(tutorialSearchIndex)};\n`,
  },
  {
    file: "data/presets/nodeTypeUsage.js",
    content: `${HEADER}export default ${jsLiteral(nodeTypeUsage)};\n`,
  },
];

let stale = false;
for (const { file, content } of outputs) {
  const fullPath = path.join(repoRoot, file);
  if (checkOnly) {
    let existing = null;
    try {
      existing = await readFile(fullPath, "utf8");
    } catch {
      // 檔案還不存在也算 stale。
    }
    if (existing !== content) {
      console.error(`STALE: ${file} does not match what generate-search-index.mjs would produce.`);
      stale = true;
    }
  } else {
    await writeFile(fullPath, content, "utf8");
    console.log(`wrote ${file}`);
  }
}

if (checkOnly) {
  if (stale) {
    console.error("\nRun `node scripts/generate-search-index.mjs` and commit the result.");
    process.exit(1);
  }
  console.log("search index files are up to date.");
}
